#include<iostream>
#include<cstring>
#include<stack>
#include<queue>
#include<climits>
#include<string>
#include<algorithm>
#include<math.h>
using namespace std;
int n;
int arr[200001];
int what, ans = INT_MIN;
int main() {
	cin >> n;
	for (int i = 1; i <= n; i++) {
		cin >> arr[i];
		what += arr[i];
		ans = max(ans, what);
		if (what < 0) what = 0;
	}
	if (ans == -0) ans = 0;
	cout << ans << endl;
	return 0;
}
